package org.commonjava.service.promote.tracking.cassandra;

import com.datastax.driver.mapping.annotations.ClusteringColumn;
import com.datastax.driver.mapping.annotations.Column;
import com.datastax.driver.mapping.annotations.PartitionKey;
import com.datastax.driver.mapping.annotations.Table;

import static org.commonjava.service.promote.tracking.cassandra.SchemaUtils.TABLE_TRACKING;

@Table( name = TABLE_TRACKING, readConsistency = "QUORUM", writeConsistency = "QUORUM" )
public class DtxPromoteRecord
{
    @PartitionKey
    private String trackingId;

    @ClusteringColumn
    private String promotionId;

    @Column
    private boolean rollback;

    @Column
    private String result;

    public String getTrackingId() {
        return trackingId;
    }

    public void setTrackingId(String trackingId) {
        this.trackingId = trackingId;
    }

    public String getPromotionId() {
        return promotionId;
    }

    public void setPromotionId(String promotionId) {
        this.promotionId = promotionId;
    }

    public String getResult() {
        return result;
    }

    public void setResult(String result) {
        this.result = result;
    }

    public boolean isRollback() {
        return rollback;
    }

    public void setRollback(boolean rollback) {
        this.rollback = rollback;
    }
}
